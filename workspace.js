function bindStudyNotesForm() {
    const toggleBtn = document.getElementById("toggle-study-add");
    const form = document.getElementById("study-note-form");
    const titleInput = document.getElementById("study-note-title");
    const contentInput = document.getElementById("study-note-content");

    if (!toggleBtn || !form) {
        return;
    }

    toggleBtn.addEventListener("click", () => {
        form.classList.toggle("hidden");
    });

    form.addEventListener("submit", (event) => {
        event.preventDefault();
        const title = titleInput.value.trim();
        const content = contentInput.value.trim();

        if (!title || !content) {
            window.alert("标题和内容都需要填写。");
            return;
        }

        const notes = readStoredList(workspaceStorageKeys.studyNotes);
        notes.push({
            id: `study-note-${Date.now()}`,
            title,
            body: content,
            tags: ["自定义", "学习笔记"]
        });
        writeStoredList(workspaceStorageKeys.studyNotes, notes);
        form.reset();
        form.classList.add("hidden");
        renderLearningNotes();
    });
}

