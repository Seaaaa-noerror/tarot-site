function getMaterialFileIcon(file) {
    if (!file) {
        return "FILE";
    }

    const name = String(file.name || file.originalName || "").toLowerCase();
    const type = String(file.type || "").toLowerCase();
    if (name.endsWith(".epub")) {
        return "EPUB";
    }
    if (name.endsWith(".pdf") || type.includes("pdf")) {
        return "PDF";
    }
    if (name.endsWith(".doc") || name.endsWith(".docx") || type.includes("word")) {
        return "DOC";
    }
    if (name.endsWith(".txt") || name.endsWith(".md") || type.startsWith("text/")) {
        return "TXT";
    }
    if (type.startsWith("image/") || /\.(png|jpe?g|webp|gif)$/i.test(name)) {
        return "IMG";
    }

    const extension = name.split(".").pop();
    return extension && extension.length <= 5 ? extension.toUpperCase() : "FILE";
}

