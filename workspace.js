async function ensureLanguageMaterialIndexes() {
    const materials = await getLanguageMaterials();
    if (!materials.some(needsMaterialIndexRebuild)) {
        return materials;
    }

    if (!materialIndexRebuildPromise) {
        materialIndexRebuildPromise = rebuildMissingMaterialIndexes(materials)
            .finally(() => {
                materialIndexRebuildPromise = null;
            });
    }

    await materialIndexRebuildPromise;
    return await getLanguageMaterials();
}

function needsMaterialIndexRebuild(material) {
    return material?.textVersion !== materialTextVersion || (material.files || []).some(needsFileIndexRebuild);
}

function needsFileIndexRebuild(file) {
    return Boolean(file?.base64) && (!file.indexStatus || typeof file.indexedChars !== "number");
}

async function rebuildMissingMaterialIndexes(materials) {
    let changed = false;

    for (const material of materials) {
        if (!needsMaterialIndexRebuild(material)) {
            continue;
        }

        const title = toSimplifiedChinese(material.title || "");
        const description = "";
        const excerpt = "";
        const files = [];
        for (const file of material.files || []) {
            if (!needsFileIndexRebuild(file)) {
                const indexedText = limitIndexedText(file.extractedText || "");
                files.push({
                    ...file,
                    originalName: file.originalName || file.name,
                    name: toSimplifiedChinese(file.name || file.originalName || ""),
                    extractedText: indexedText,
                    indexedChars: indexedText.length,
                    indexStatus: toSimplifiedChinese(file.indexStatus || "已保存文件")
                });
                continue;
            }

            try {
                const blob = base64ToBlob(file.base64, file.type || "application/octet-stream");
                const restoredFile = {
                    name: file.name,
                    type: file.type || "application/octet-stream",
                    size: blob.size,
                    text: () => blob.text(),
                    arrayBuffer: () => blob.arrayBuffer()
                };
                const extraction = await extractTextForSearch(restoredFile);
                const indexedText = limitIndexedText(extraction.text || "");
                files.push({
                    ...file,
                    originalName: file.originalName || file.name,
                    name: toSimplifiedChinese(file.name || file.originalName || ""),
                    extractedText: indexedText,
                    indexedChars: indexedText.length,
                    indexStatus: toSimplifiedChinese(extraction.status)
                });
            } catch (_error) {
                const fallbackText = limitIndexedText(file.extractedText || `${file.name} ${file.type || ""}`);
                files.push({
                    ...file,
                    originalName: file.originalName || file.name,
                    name: toSimplifiedChinese(file.name || file.originalName || ""),
                    extractedText: fallbackText,
                    indexedChars: fallbackText.length,
                    indexStatus: "旧文件索引重建失败，仅保留原有可检索内容"
                });
            }
        }

        await saveLanguageMaterial({
            ...material,
            title,
            description,
            excerpt,
            files,
            searchableText: limitIndexedText([
                title,
                ...files.map((file) => file.extractedText || "")
            ].join("\n")),
            indexedAt: new Date().toISOString(),
            textVersion: materialTextVersion
        });
        changed = true;
    }

    return changed;
}

function openLanguageDb() {
    return new Promise((resolve, reject) => {
        const request = indexedDB.open(fileDbName, 1);
        request.onupgradeneeded = () => {
            const db = request.result;
            if (!db.objectStoreNames.contains(fileStoreName)) {
                db.createObjectStore(fileStoreName, { keyPath: "id" });
            }
        };
        request.onsuccess = () => resolve(request.result);
        request.onerror = () => reject(request.error);
    });
}

