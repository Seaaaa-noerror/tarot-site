function renderMonthCalendar(entries) {
    const node = document.getElementById("month-calendar");
    const titleNode = document.getElementById("calendar-month-title");
    if (!node || !titleNode) {
        return;
    }

    const datePicker = document.getElementById("calendar-date-jump");
    if (datePicker && datePicker.value !== selectedCalendarDate) {
        datePicker.value = selectedCalendarDate;
    }

    node.innerHTML = "";
    titleNode.textContent = `${calendarCursor.getFullYear()} 年 ${calendarCursor.getMonth() + 1} 月回顾`;

    ["一", "二", "三", "四", "五", "六", "日"].forEach((weekday) => {
        const label = document.createElement("div");
        label.className = "calendar-weekday";
        label.textContent = weekday;
        node.appendChild(label);
    });

    const monthStart = new Date(calendarCursor.getFullYear(), calendarCursor.getMonth(), 1);
    const monthEnd = new Date(calendarCursor.getFullYear(), calendarCursor.getMonth() + 1, 0);
    const firstWeekday = monthStart.getDay() || 7;

    for (let empty = 1; empty < firstWeekday; empty++) {
        const blank = document.createElement("div");
        blank.className = "calendar-day is-empty";
        node.appendChild(blank);
    }

    for (let day = 1; day <= monthEnd.getDate(); day++) {
        const date = new Date(calendarCursor.getFullYear(), calendarCursor.getMonth(), day);
        const dayKey = formatDateInput(date);
        const dateLabel = `${date.getMonth() + 1}.${day}`;
        const weekdayLabel = ["周日", "周一", "周二", "周三", "周四", "周五", "周六"][date.getDay()];
        const dayEntries = entries
            .filter((item) => item.date === dayKey)
            .sort((a, b) => getJournalEntryTimeValue(b) - getJournalEntryTimeValue(a));
        const dayCards = [...new Set(dayEntries.flatMap((entry) => entry.cards || []))];
        const preview = dayCards.join("、");

        const card = document.createElement("article");
        card.className = `calendar-day${dayEntries.length ? " has-record" : ""}${dayKey === selectedCalendarDate ? " is-selected-date" : ""}`;
        if (preview) {
            card.dataset.preview = preview;
        }
        if (dayEntries.length) {
            card.innerHTML = `
                <a class="calendar-entry-link" href="journal-entry.html?date=${encodeURIComponent(dayKey)}" aria-label="查看 ${dayKey} 的日记记录">
                    <span class="calendar-entry-head">
                        <span class="calendar-date">
                            <span class="calendar-day-number">${escapeHtml(dateLabel)}</span>
                            <span class="calendar-week-label">${escapeHtml(weekdayLabel)}</span>
                        </span>
                        <span class="calendar-entry-count">${dayEntries.length} 条记录</span>
                    </span>
                    <span class="calendar-cards">
                        ${dayCards.length
                            ? dayCards.map((name) => `<span class="tag">${escapeHtml(name)}</span>`).join("")
                            : `<span class="tag">有日记记录</span>`}
                    </span>
                </a>
            `;
        } else {
            card.innerHTML = `
                <span class="calendar-date">
                    <span class="calendar-day-number">${escapeHtml(dateLabel)}</span>
                    <span class="calendar-week-label">${escapeHtml(weekdayLabel)}</span>
                </span>
            `;
        }
        node.appendChild(card);
    }
}

