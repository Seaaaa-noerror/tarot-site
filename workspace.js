function toSimplifiedChinese(text) {
    let value = String(text || "");
    if (!value) {
        return "";
    }

    traditionalPhraseMap.forEach(([from, to]) => {
        value = value.replaceAll(from, to);
    });

    return value.replace(traditionalCharRegex, (char) => traditionalCharMap[char] || char);
}

