async function extractTextForSearch(file) {
    const lowerName = file.name.toLowerCase();

    try {
        if (file.type.startsWith("text/") || [".txt", ".md", ".csv", ".json"].some((ext) => lowerName.endsWith(ext))) {
            return {
                text: await file.text(),
                status: "已从文本文件建立索引"
            };
        }

        if (lowerName.endsWith(".epub")) {
            const text = await extractEpubText(file);
            return {
                text,
                status: text ? "已从 EPUB 正文建立索引" : "EPUB 未提取到正文，仅索引文件名"
            };
        }

        if (lowerName.endsWith(".docx")) {
            const text = await extractDocxText(file);
            return {
                text,
                status: text ? "已从 Word 正文建立索引" : "Word 未提取到正文，仅索引文件名"
            };
        }

        if (lowerName.endsWith(".pdf") || file.type === "application/pdf") {
            const text = await extractPdfText(file);
            return {
                text,
                status: text ? "已从 PDF 可读文本建立索引" : "PDF 未提取到可读正文，仅索引文件名"
            };
        }
    } catch (_error) {
        return {
            text: `${file.name} ${file.type || ""}`,
            status: "索引时遇到限制，仅索引文件名"
        };
    }

    return {
        text: `${file.name} ${file.type || ""}`,
        status: "该格式暂不支持自动提取正文，仅索引文件名"
    };
}

async function extractEpubText(file) {
    const entries = await readZipEntries(file, (name) => /\.(xhtml|html|htm|xml|ncx)$/i.test(name));
    return limitIndexedText(entries
        .filter((entry) => !/container\.xml$/i.test(entry.name))
        .map((entry) => htmlToPlainText(entry.text))
        .join("\n"));
}

async function extractDocxText(file) {
    const entries = await readZipEntries(file, (name) => /^word\/(document|footnotes|endnotes|comments|header\d+|footer\d+)\.xml$/i.test(name));
    return limitIndexedText(entries
        .map((entry) => xmlToPlainText(entry.text))
        .join("\n"));
}

async function readZipEntries(file, shouldReadEntry) {
    const bytes = new Uint8Array(await file.arrayBuffer());
    const eocdOffset = findEndOfCentralDirectory(bytes);
    if (eocdOffset < 0) {
        return [];
    }

    const view = new DataView(bytes.buffer, bytes.byteOffset, bytes.byteLength);
    const centralDirectoryOffset = view.getUint32(eocdOffset + 16, true);
    const entryCount = view.getUint16(eocdOffset + 10, true);
    const entries = [];
    let offset = centralDirectoryOffset;

    for (let index = 0; index < entryCount; index += 1) {
        if (view.getUint32(offset, true) !== 0x02014b50) {
            break;
        }

        const method = view.getUint16(offset + 10, true);
        const compressedSize = view.getUint32(offset + 20, true);
        const fileNameLength = view.getUint16(offset + 28, true);
        const extraLength = view.getUint16(offset + 30, true);
        const commentLength = view.getUint16(offset + 32, true);
        const localHeaderOffset = view.getUint32(offset + 42, true);
        const nameBytes = bytes.slice(offset + 46, offset + 46 + fileNameLength);
        const name = decodeUtf8(nameBytes);

        if (shouldReadEntry(name)) {
            const localNameLength = view.getUint16(localHeaderOffset + 26, true);
            const localExtraLength = view.getUint16(localHeaderOffset + 28, true);
            const dataStart = localHeaderOffset + 30 + localNameLength + localExtraLength;
            const compressed = bytes.slice(dataStart, dataStart + compressedSize);
            const data = await unzipEntryBytes(compressed, method);
            if (data.length) {
                entries.push({
                    name,
                    text: decodeUtf8(data)
                });
            }
        }

        offset += 46 + fileNameLength + extraLength + commentLength;
    }

    return entries;
}

function findEndOfCentralDirectory(bytes) {
    for (let index = bytes.length - 22; index >= 0; index -= 1) {
        if (
            bytes[index] === 0x50 &&
            bytes[index + 1] === 0x4b &&
            bytes[index + 2] === 0x05 &&
            bytes[index + 3] === 0x06
        ) {
            return index;
        }
    }

    return -1;
}

async function unzipEntryBytes(bytes, method) {
    if (method === 0) {
        return bytes;
    }

    if (method === 8) {
        return await decompressBytes(bytes, "deflate-raw");
    }

    return new Uint8Array();
}

async function decompressBytes(bytes, format) {
    if (!("DecompressionStream" in window)) {
        return new Uint8Array();
    }

    const formats = format === "deflate-raw" ? ["deflate-raw", "deflate"] : [format, "deflate-raw"];
    for (const item of formats) {
        try {
            const stream = new Blob([bytes]).stream().pipeThrough(new DecompressionStream(item));
            return new Uint8Array(await new Response(stream).arrayBuffer());
        } catch (_error) {
            // Try the next compatible compression wrapper.
        }
    }

    return new Uint8Array();
}

async function extractPdfText(file) {
    const bytes = new Uint8Array(await file.arrayBuffer());
    const raw = bytesToBinaryString(bytes);
    const chunks = [extractPdfVisibleText(raw)];

    const streams = await extractPdfStreamTexts(bytes, raw);
    chunks.push(...streams);

    return limitIndexedText(chunks.join("\n"));
}

async function extractPdfStreamTexts(bytes, raw) {
    const chunks = [];
    let position = 0;

    while (position < raw.length) {
        const streamIndex = raw.indexOf("stream", position);
        if (streamIndex < 0) {
            break;
        }

        const endIndex = raw.indexOf("endstream", streamIndex);
        if (endIndex < 0) {
            break;
        }

        const dictStart = raw.lastIndexOf("<<", streamIndex);
        const dictEnd = raw.lastIndexOf(">>", streamIndex);
        const dictionary = dictStart >= 0 && dictEnd >= dictStart ? raw.slice(dictStart, dictEnd + 2) : "";
        let dataStart = streamIndex + "stream".length;
        if (raw[dataStart] === "\r" && raw[dataStart + 1] === "\n") {
            dataStart += 2;
        } else if (raw[dataStart] === "\n" || raw[dataStart] === "\r") {
            dataStart += 1;
        }

        let dataEnd = endIndex;
        while (dataEnd > dataStart && (raw[dataEnd - 1] === "\n" || raw[dataEnd - 1] === "\r")) {
            dataEnd -= 1;
        }

        const streamBytes = bytes.slice(dataStart, dataEnd);
        if (/FlateDecode/.test(dictionary)) {
            const inflated = await decompressBytes(streamBytes, "deflate");
            if (inflated.length) {
                chunks.push(extractPdfVisibleText(bytesToBinaryString(inflated)));
            }
        } else {
            chunks.push(extractPdfVisibleText(bytesToBinaryString(streamBytes)));
        }

        position = endIndex + "endstream".length;
    }

    return chunks;
}

function extractPdfVisibleText(raw) {
    const chunks = [];
    const operatorPattern = /(?:\[(?:[\s\S]{0,3000}?)\]|\((?:\\.|[^\\()])*\)|<[\da-fA-F\s]+>)\s*T[Jj]/g;
    const matches = raw.match(operatorPattern) || [];

    matches.forEach((segment) => {
        const pieces = [];
        [...segment.matchAll(/\((?:\\.|[^\\()])*\)/g)].forEach((match) => {
            pieces.push(decodePdfLiteralString(match[0]));
        });
        [...segment.matchAll(/<([\da-fA-F\s]+)>/g)].forEach((match) => {
            pieces.push(decodePdfHexString(match[1]));
        });

        const line = pieces.join("").trim();
        if (line) {
            chunks.push(line);
        }
    });

    return normalizeIndexedText(chunks.join("\n"));
}

function decodePdfLiteralString(value) {
    return value
        .slice(1, -1)
        .replace(/\\([nrtbf()\\])/g, (_match, escaped) => {
            const replacements = {
                n: "\n",
                r: "\r",
                t: "\t",
                b: "\b",
                f: "\f",
                "(": "(",
                ")": ")",
                "\\": "\\"
            };
            return replacements[escaped] || escaped;
        })
        .replace(/\\([0-7]{1,3})/g, (_match, octal) => String.fromCharCode(parseInt(octal, 8)))
        .replace(/\\\r?\n/g, "");
}

function decodePdfHexString(value) {
    const clean = value.replace(/\s+/g, "");
    const bytes = new Uint8Array(Math.ceil(clean.length / 2));
    for (let index = 0; index < bytes.length; index += 1) {
        bytes[index] = parseInt(clean.slice(index * 2, index * 2 + 2).padEnd(2, "0"), 16);
    }

    if (bytes[0] === 0xfe && bytes[1] === 0xff) {
        return decodeUtf16Be(bytes.slice(2));
    }

    if (bytes.filter((item) => item === 0).length > bytes.length / 4) {
        return decodeUtf16Be(bytes);
    }

    return decodeUtf8(bytes);
}

function decodeUtf16Be(bytes) {
    let output = "";
    for (let index = 0; index < bytes.length - 1; index += 2) {
        output += String.fromCharCode((bytes[index] << 8) + bytes[index + 1]);
    }
    return output;
}

function htmlToPlainText(value) {
    const withoutNoise = String(value || "")
        .replace(/<script[\s\S]*?<\/script>/gi, " ")
        .replace(/<style[\s\S]*?<\/style>/gi, " ")
        .replace(/<\/(p|div|section|article|h[1-6]|li|br)>/gi, "\n");
    const documentNode = new DOMParser().parseFromString(withoutNoise, "text/html");
    return normalizeIndexedText(documentNode.body?.textContent || withoutNoise.replace(/<[^>]+>/g, " "));
}

function xmlToPlainText(value) {
    const spaced = String(value || "")
        .replace(/<\/w:p>/g, "\n")
        .replace(/<\/w:tab>/g, "\t")
        .replace(/<[^>]+>/g, " ");
    return decodeHtmlEntities(normalizeIndexedText(spaced));
}

function decodeHtmlEntities(value) {
    const textarea = document.createElement("textarea");
    textarea.innerHTML = value;
    return textarea.value;
}

function decodeUtf8(bytes) {
    return new TextDecoder("utf-8").decode(bytes);
}

function bytesToBinaryString(bytes) {
    let result = "";
    const chunkSize = 8192;
    for (let index = 0; index < bytes.length; index += chunkSize) {
        result += String.fromCharCode(...bytes.slice(index, index + chunkSize));
    }
    return result;
}

function normalizeIndexedText(text) {
    return toSimplifiedChinese(text)
        .replace(/\u0000/g, "")
        .replace(/\s+/g, " ")
        .trim();
}

function limitIndexedText(text) {
    const normalized = normalizeIndexedText(text);
    if (normalized.length <= maxIndexedTextLength) {
        return normalized;
    }

    return normalized.slice(0, maxIndexedTextLength);
}

function blobToBase64(blob) {
    return new Promise((resolve, reject) => {
        const reader = new FileReader();
        reader.onload = () => {
            const result = String(reader.result);
            resolve(result.split(",")[1] || "");
        };
        reader.onerror = () => reject(reader.error);
        reader.readAsDataURL(blob);
    });
}

function base64ToBlob(base64, type) {
    const binary = atob(base64);
    const bytes = new Uint8Array(binary.length);
    for (let index = 0; index < binary.length; index++) {
        bytes[index] = binary.charCodeAt(index);
    }
    return new Blob([bytes], { type });
}

function downloadStoredFile(file) {
    const blob = base64ToBlob(file.base64, file.type);
    const url = URL.createObjectURL(blob);
    const link = document.createElement("a");
    link.href = url;
    link.download = file.name;
    document.body.appendChild(link);
    link.click();
    link.remove();
    setTimeout(() => URL.revokeObjectURL(url), 1000);
}

function escapeHtml(text) {
    return String(text)
        .replaceAll("&", "&amp;")
        .replaceAll("<", "&lt;")
        .replaceAll(">", "&gt;")
        .replaceAll("\"", "&quot;")
        .replaceAll("'", "&#39;");
}

function initUsageModal() {
    const modal = document.getElementById("usage-modal");
    const closeBtn = document.getElementById("usage-modal-close");
    const sessionKey = `usageModalSeen:${window.location.pathname}`;

    if (!modal || sessionStorage.getItem(sessionKey) === "1") {
        return;
    }

    modal.classList.remove("hidden");
    modal.setAttribute("aria-hidden", "false");

    const close = () => {
        modal.classList.add("hidden");
        modal.setAttribute("aria-hidden", "true");
        sessionStorage.setItem(sessionKey, "1");
    };

    closeBtn?.addEventListener("click", close);
    modal.querySelectorAll("[data-close-usage='true']").forEach((node) => {
        node.addEventListener("click", close);
    });
}
