async function renderJournalReferenceMatches() {
    const listNode = document.getElementById("journal-reference-list");
    const contentInput = document.getElementById("journal-content");
    if (!listNode || !contentInput) {
        return;
    }

    const queryText = `${journalSelectedCards.join(" ")} ${contentInput.value.trim()}`.trim();
    if (!queryText) {
        listNode.innerHTML = `<div class="empty-list">先选择牌面或写下回顾内容，这里会自动检索素材库中可能相关的资料。</div>`;
        return;
    }

    listNode.innerHTML = `<div class="empty-list">正在检索素材库文字索引...</div>`;

    const materials = await ensureLanguageMaterialIndexes();
    if (!materials.length) {
        listNode.innerHTML = `<div class="empty-list">素材库里还没有可检索资料。请先去素材库上传文件，系统会自动建立文字索引。</div>`;
        return;
    }

    const keywords = tokenize(queryText);
    const matches = materials
        .map((material) => {
            const score = scoreMaterialMatch(material, keywords);
            return {
                material,
                score,
                snippets: buildMaterialMatchSnippets(material, keywords)
            };
        })
        .filter((item) => item.score > 0)
        .sort((a, b) => b.score - a.score)
        .slice(0, 6);

    if (!matches.length) {
        listNode.innerHTML = `<div class="empty-list">素材库里暂时没有命中当前日记内容的资料。你可以去素材库补充摘要、摘录或可检索文本。</div>`;
        return;
    }

    listNode.innerHTML = "";
    matches.forEach(({ material, score, snippets }) => {
        const card = document.createElement("article");
        card.className = "guide-card";
        card.innerHTML = `
            <p class="guide-card-meta">匹配度 ${score}</p>
            <h3>${escapeHtml(toSimplifiedChinese(material.title))}</h3>
            ${snippets.length ? `
                <div class="reference-snippet-list">
                    ${snippets.map((snippet) => `
                        <a class="reference-snippet" href="${escapeHtml(buildMaterialReaderUrl(material.id, snippet))}">
                            <span>${escapeHtml(toSimplifiedChinese(snippet.source))} · 点击定位</span>
                            ${escapeHtml(toSimplifiedChinese(snippet.text))}
                        </a>
                    `).join("")}
                </div>
            ` : ""}
            <div class="card-actions">
                ${(material.files || []).map((file) => `<button class="text-btn" type="button" data-material-download="${escapeHtml(material.id)}" data-file-name="${escapeHtml(toSimplifiedChinese(file.name || file.originalName || ""))}">下载 ${escapeHtml(toSimplifiedChinese(file.name || file.originalName || ""))}</button>`).join("")}
            </div>
        `;
        listNode.appendChild(card);
    });

    listNode.querySelectorAll("[data-material-download]").forEach((button) => {
        button.addEventListener("click", async () => {
            const material = await getLanguageMaterialById(button.dataset.materialDownload);
            const file = material?.files?.find((item) => toSimplifiedChinese(item.name || item.originalName || "") === button.dataset.fileName);
            if (!file) {
                return;
            }
            downloadStoredFile(file);
        });
    });
}

